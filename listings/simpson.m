% Функция
f = @(x) 3 * x.^5 + 2 * x.^4 - 5 * x.^3 - 3 * x.^2 + x - 3;
% Диапазон x
a = -1; b = 1;
N = 4; % Кол-во отрезков
h = (b - a) / N; % Шаг
x = @(j) a + j * h;
S1 = 0; % первая сумма в формуле
S2 = 0; % вторая сумма в формуле
for j = 1:(N / 2 - 1)
    S1 = S1 + f(x(2 * j));
end
for j = 1:(N / 2)
    S2 = S2 + f(x(2 * j - 1));
end
Is = h / 3 * (f(x(0)) + 2 * S1 + 4 * S2 + f(x(N)));
Rs = abs(I - Is);